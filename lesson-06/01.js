nextBtn.onclick = function () {
    console.log('next slide')
    showNextSlide('next')
}

prevBtn.onclick = function () {
    console.log('prev slide')
    showNextSlide('prev')
}

function showNextSlide (direction) {
    // –°–∫—Ä—ã–≤–∞–µ–º —Ç–µ–∫—É—â–∏–π —Å–ª–∞–π–¥
    const currentSlide = mySlider.querySelector('[data-active]')
    const currentSlideIndex = +currentSlide.dataset.index
    currentSlide.style.display = 'none'
    currentSlide.removeAttribute('data-active')

    //–†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ–º —Å–ª–µ–¥—É—é—â–∏–π –∏–Ω–¥–µ–∫—Å
    let nextSlideIndex
    if(direction === 'next') {
        nextSlideIndex = currentSlideIndex + 1 === sliderItems.length ? 0 : currentSlideIndex + 1
    } else if (direction === 'prev') {
        nextSlideIndex = currentSlideIndex === 0 ? sliderItems.length - 1 : currentSlideIndex - 1
    }

    //–ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Å–ª–µ–¥—É—é—â–∏–π —Å–ª–∞–π–¥
    const nextSlide = mySlider.querySelector(`[data-index="${nextSlideIndex}"]`)
    nextSlide.style.display = 'block'
    nextSlide.setAttribute('data-active', '')
}

